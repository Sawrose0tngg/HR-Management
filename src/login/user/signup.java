    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String uname = jtusername.getText();
        if(uname.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Username is empty","Error",JOptionPane.WARNING_MESSAGE);
            return;
        }
        String pass1 = jpass1.getText();
        if(pass1.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Password is empty","Error",JOptionPane.WARNING_MESSAGE);
            return;
        }
        String pass2 = jpass2.getText();
        if(pass2.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Confirm your password","Error",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!pass1.equals(pass2))
        {
            JOptionPane.showMessageDialog(this, "Passwords did not match","Error",JOptionPane.WARNING_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(this, "Successful","Sign up",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

